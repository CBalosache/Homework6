using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TestDrivenDevelopment
{
    class Program
    {
        static void Main(string[] args)
        {
            // Test driven development steps
            //1. Get the requirement from the user - what App to build
            //2. Give the requirement to architect/software designer for overview
            //3. Receive specification from analyst to developer
            //4. for each item on the requirement specification determine the proposed function
            //5. for each function we can calculate the expected input and output
            //6. from these input and output the developer can write some test which expect this output given input
            //7. the developer add these tests to our application and run it
            //8. the test will fail
            //9. the developer write the minimum code to pass the test
            //10. once the test goes from red to green i.e. fail to pass the developer are done
            //11. final step is to refactor developer's code to ensure it is efficient as possible
            //12. repeat step 6 to 11 until all items in the requirement specification is done
        }
    }
}
